namespace Jade.Input;

/// <summary>
/// Represents the axes available on a gamepad.
/// </summary>
public enum GamepadAxis
{
    /// <summary>
    /// The horizontal axis of the left stick.
    /// </summary>
    LeftStickX,

    /// <summary>
    /// The vertical axis of the left stick.
    /// </summary>
    LeftStickY,

    /// <summary>
    /// The horizontal axis of the right stick.
    /// </summary>
    RightStickX,

    /// <summary>
    /// The vertical axis of the right stick.
    /// </summary>
    RightStickY,

    /// <summary>
    /// The left trigger axis.
    /// </summary>
    LeftTrigger,

    /// <summary>
    /// The right trigger axis.
    /// </summary>
    RightTrigger
}
