namespace Jade.Input;

/// <summary>
/// Represents the buttons available on a gamepad.
/// </summary>
public enum GamepadButton
{
    /// <summary>
    /// The A button on the gamepad.
    /// </summary>
    A,

    /// <summary>
    /// The B button on the gamepad.
    /// </summary>
    B,

    /// <summary>
    /// The X button on the gamepad.
    /// </summary>
    X,

    /// <summary>
    /// The Y button on the gamepad.
    /// </summary>
    Y,

    /// <summary>
    /// The left bumper button on the gamepad.
    /// </summary>
    LeftBumper,

    /// <summary>
    /// The right bumper button on the gamepad.
    /// </summary>
    RightBumper,

    /// <summary>
    /// The back button on the gamepad.
    /// </summary>
    Back,

    /// <summary>
    /// The start button on the gamepad.
    /// </summary>
    Start,

    /// <summary>
    /// The home button on the gamepad.
    /// </summary>
    Home,

    /// <summary>
    /// The left stick button on the gamepad.
    /// </summary>
    LeftStick,

    /// <summary>
    /// The right stick button on the gamepad.
    /// </summary>
    RightStick,

    /// <summary>
    /// The D-pad up button on the gamepad.
    /// </summary>
    DPadUp,

    /// <summary>
    /// The D-pad right button on the gamepad.
    /// </summary>
    DPadRight,

    /// <summary>
    /// The D-pad down button on the gamepad.
    /// </summary>
    DPadDown,

    /// <summary>
    /// The D-pad left button on the gamepad.
    /// </summary>
    DPadLeft
}
