namespace Jade.Input;

/// <summary>
/// Represents the state of a button in an input device.
/// </summary>
public enum ButtonState
{
    /// <summary>
    /// Indicates that the button is not being pressed.
    /// </summary>
    Released,

    /// <summary>
    /// Indicates that the button is being pressed.
    /// </summary>
    Pressed
}
